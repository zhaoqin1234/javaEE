package com.hrxt.pojo;

/**
 * 
 * @author ZhaoQin
 *
 */
public class Well {
	
	
	private String wellId;
	private String wellCommonName;
	private String siteId;  //断块————同指油藏
	private String siteName;
	private String orgId;   //工区  
	private String orgName;
	private String jd;		//井段
	private String hd;		//厚度
	private String cs;		//层数
	
	
	public String getJd() {
		return jd;
	}
	public void setJd(String jd) {
		this.jd = jd;
	}
	public String getHd() {
		return hd;
	}
	public void setHd(String hd) {
		this.hd = hd;
	}
	public String getCs() {
		return cs;
	}
	public void setCs(String cs) {
		this.cs = cs;
	}
	public String getWellId() {
		return wellId;
	}
	public void setWellId(String wellId) {
		this.wellId = wellId;
	}
	public String getWellCommonName() {
		return wellCommonName;
	}
	public void setWellCommonName(String wellCommonName) {
		this.wellCommonName = wellCommonName;
	}
	public String getSiteId() {
		return siteId;
	}
	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}
	public String getSiteName() {
		return siteName;
	}
	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	@Override
	public String toString() {
		return "Well [wellId=" + wellId + ", wellCommonName=" + wellCommonName + ", siteId=" + siteId + ", siteName="
				+ siteName + ", orgId=" + orgId + ", orgName=" + orgName + "]";
	}
	
	
	
	
}
